<h2>1. Ennen rekisteröintiä</h2>

<h3><a name="lomake"></a>1.1. Lomakkeen täyttäminen ja käsittely</h3>
	<p>Vain VRL-tunnuksen rekisteröineet jäsenet voivat rekisteröidä tallejaan virtuaalitallirekisteriin. 

	<p>Voit poistaa oman tallisi rekisteristä koska tahansa, jos siellä ei ole järjestetty kilpailuja tai näyttelyitä, ole syntynyt kasvatteja tai asu hevosia. Muussa tapauksessa tallia ei voi poistaa. Jos kuitenkin huomaat virheen tallisi rekisteriprofiilissa, otathan yhteyttä <a href="<?php echo site_url('liitto/tiedotus/728') ?>" title="Ota yhteyttä tukeen">tukeen</a>.</p>



<h3><a name="ehdot"></a>1.2. Rekisteröintiehdot</h3>
	<p>Tallilla tulee olla oma <b>valmis</b> sivu tai sivusto. Lisäksi tallin sivuilla tulee olla maininta virtuaalisuudesta! Lue kohta: <a href="#teksti">Virtuaalitalli-merkintä</a>. Virtuaalisen ratsastajainliiton alaiseksi virtuaalitalleiksi ei hyväksytä esimerkiksi eläinhoitoloita (joissa ei ole hevosia) tai satuolentohoitoloita/-talleja.</p>

	<p>Sivuilta ei saa löytyä laitonta tai loukkaavaa materiaalia ja tekijänoikeudet tulee olla merkitty asianmukaisesti.</p>


<h3><a name="teksti"></a>1.3. Virtuaalitalli-merkintä</h3>
	<p>Tallin kaikilta <u>pääsivuilta</u> tulee löytyä näkyvä kirjoitettu teksti "<strong>Tämä on virtuaalitalli</strong>" tai vastaava lause, jossa on sana "<strong>virtuaalitalli</strong>" tässä muodossaan. Merkintä otsikkokuvassa, jossain muussa kuvassa, titlessä (sivun otsikossa) tai muu muoto, kuten "virtuaalinen talli", ei kelpaa. Merkinnän tulee löytyä CTRL + F -selainhaulla.</p>

	<p>Merkinnällä pyritään estämään virtuaalitallien sekoittaminen oikeisiin talleihin ja mahdollistamaan virtuaalitallien poistaminen hakukoneiden hakutuloksista käyttämällä hakusanaa "virtuaalitalli".</p>


<h3><a name="tarkistus"></a>1.4. Muista tarkistaa</h3>
	<p>Onko tallin nimi ja www-osoite kirjoitettu oikein lomakkeeseen? </p>

	<p>Onhan tallisi ensisijainen kategoria oikein? Tallin täytyy täyttää kategorian vaatimukset jo rekisteröintivaiheessa. Tarkastathan kategoriavaatimukset alla olevasta listasta (2. Kategoriat).</span></p>

<h2>2. Kategoriat</h2>
	

	<h3><a name="kat0"></a>2.0. Harrastetalli (KAT0)</h3>
	<p>Harrastetallille ei ole mitään toimintavaatimuksia. Mikäli tallisi ei sovellu mihinkään muuhun kategoriaan, valitse tämä kategoria. Huomaa, että voit edelleen pitää halutessasi tallisi nimessä minkä tahansa kategorialiitteen (esim. yksityistalli, pientalli..), vaikka rekisteröisit sen harrastetalliksi! </p>
	
	<h3><a name="kat1"></a>2.1. Ratsastuskoulu (KAT1)</h3>
	<p>Ratsastuskoulun toiminnassa ja tallin sivuston sisällössä tulee näkyä jonkinlainen tunti- tai hoitajatoiminta.</p>
	
	<h3><a name="kat2"></a>2.2. Siittola (KAT2)</h3>
	<p>Siittolassa tulee harjoittaa kasvatustoimintaa. Sivulta olisi hyvä löytyä jonkinlainen selvitys siittolan kasvatustavoitteista ja -periaatteista.</p>
	
	<h3><a name="kat3"></a>2.3. Kilpailukeskus (KAT3)</h3>
	<p>Kilpailukeskuksessa järjestetään ensisijaisesti kisatoimintaa.</p>
	
	<h3><a name="kat4"></a>2.4. Kilpatallit, mahd. painotus / laukka-, ravi-, western- ja valjakkotallit (KAT4)</h3>
	<p>Kilpatallilta tulee löytyä (tallin painotuslajissa, jos sellainen on) kilpailevia ja siihen soveltuvia hevosia tai kuvaus tallin hevosten käyttötarkoituksesta.</p>
	
	<h3><a name="kat5"></a>2.5. Myynti-/tuontitallit (KAT5)</h3>
	<p>Myynti- tai tuontitalleilla ensisijainen toiminta on hevosten myynti tai niiden tuonti virtuaalimaailmaan. Tallin sivulta tulee löytyä ohjeet hevosen ostamiseen.</p>
	
	<h3><a name="kat6"></a>2.6. Oriasema (KAT6)</h3>
	<p>Oriasemilta tulee löytyä selkeästi kuvaus toiminnasta sekä astutustoimintaan liittyvät ohjeistukset, kuten esimerkiksi tammojen vaatimukset sekä ohjeet astutuspyynnön lähettämiseen.</p>
	
	<h3><a name="kat7"></a>2.7. Yksityistalli (KAT7)</h3>
	<p>Yksityistalli majoittaa hevosia ja tarjoaa tallipaikkoja myös ulkopuolisille. Tallin sivuilta tulee löytyä vaatimukset ja ohjeet yksityiseksi pääsemiseksi.</p>